package CiclosAnidados;
public class IncrementoTriangulos {
	public static void main(String[] args) {
		for(int i=1; i<=5; i++){
			//System.out.print('*');
			for(int j=1; j<=i; j++) {
				//System.out.print(j + "*");
				System.out.print('*');
			}//for j
			System.out.println(" ");
		}//for i
	}//main
}//class
